package homework3;

public class SimpleCal {
	 
	static double add(double n1, double n2) {
         return n1+n2;
      }
     static double sub(double n1, double n2) {
         return n1-n2;
      }
      static double mul(double n1, double n2) {
         return n1*n2;
      }
      static double div(double n1, double n2) {
         return n1/n2;
      }

}